    private void input_table(ArrayList<String> vaccArray){ //call this function whenever need to update the table
                                //use the arraylist to input table
        DefaultTableModel model = (DefaultTableModel)vacc_table.getModel();//The table variable
        model.setRowCount(0);//clear table
        try{
            for(String vac:vaccArray){
                //insert a data into table
                String[] data_row = vac.split(",");
                if(data_row[3].equals("0")){}
                else{
                    String[] selected_data={data_row[0],data_row[1],data_row[2],data_row[3]};   //must select the data in each array into the table
                    model.addRow(selected_data);
                }
            }
        }
        catch(Exception e){
            System.out.println("Table error");
        }
    }
    
    private void vacc_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_vacc_tableMouseClicked
        // TODO add your handling code here:
        int i = vacc_table.getSelectedRow();
        TableModel model=vacc_table.getModel();
        int id = Integer.parseInt(model.getValueAt(i, 0).toString());
        ArrayList<String> selected_data = vacc.search_byVacID(id);
        Vaccine vac = new Vaccine(selected_data.get(0));
        lblID.setText(String.valueOf(vac.getVacID()));
        cmbVaccTemp.setSelectedItem(vac.getVacName());
        cmbLocationTemp.setSelectedItem(vac.getVacLoc());
        cmbVaccTemp.setEnabled(false);
        cmbLocationTemp.setEnabled(false);
        txtSupply.setText(vac.getVacSupp());//assign the data to the label panel
        btnAdd.setEnabled(false);//make add supply unclickable
        btnModify.setEnabled(true);
        btnRmv.setEnabled(true);
        btnClear.setEnabled(true);//make the other function clickable
    }//GEN-LAST:event_vacc_tableMouseClicked

