    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try{
            ArrayList<String> vac_data_arrlist=new ArrayList<>();
            int id=Integer.parseInt(txtID.getText());
            String vac_name=(String) cmbVacc.getSelectedItem();
            String vac_loc=(String) cmbLocation.getSelectedItem();

            if(vac_name=="All" && vac_loc=="All"){
                vac_data_arrlist = vacc.search_byVacID(id);
                if (vac_data_arrlist==null){
                    JOptionPane.showMessageDialog(null,"ID Not Found");
                    txtID.setText("Search by ID");
                } else{ //display to the table
                    input_table(vac_data_arrlist);
                }
            }
            else if(vac_loc=="All"){//search according to vac name and id
                vac_data_arrlist = vacc.search_by_id_name(id,vac_name); //

                if (vac_data_arrlist==null){
                    JOptionPane.showMessageDialog(null,"ID Not Found");
                    txtID.setText("Search by ID");
                } else{ //display to the table
                    input_table(vac_data_arrlist);
                }
            }
            //all selected
            else if(vac_name=="All"){//search according to vac location and id
                vac_data_arrlist = vacc.search_by_id_loc(id,vac_loc); //

                if (vac_data_arrlist==null){
                    JOptionPane.showMessageDialog(null,"ID Not Found");
                    txtID.setText("Search by ID");
                } else{ //display to the table
                    input_table(vac_data_arrlist);
                }
            }
            else{//search according to vac name, location, and id
                vac_data_arrlist = vacc.search(id,vac_name,vac_loc);
                if (vac_data_arrlist==null){
                    JOptionPane.showMessageDialog(null,"ID Not Found");
                    txtID.setText("Search by ID");
                } else{ //display to the table
                    input_table(vac_data_arrlist);
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Please input correct ID");
            txtID.setText("Search by ID");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void check_cmbbox(){
        ArrayList<String> vac_data_arrlist=new ArrayList<>();
        String vac_name=(String) cmbVacc.getSelectedItem();
        String vac_loc=(String) cmbLocation.getSelectedItem();
        //all all
        if(vac_name=="All" && vac_loc=="All"){//view all detail
            ArrayList<String> vacArray = vacc.view_all_details();
            input_table(vacArray);
        }
        //selected all //check name
        else if(vac_loc=="All"){//search vac name
            vac_data_arrlist = vacc.search_byVacName(vac_name);
            input_table(vac_data_arrlist);
        }
        //all selected
        else if(vac_name=="All"){//search vac loc
            vac_data_arrlist = vacc.search_byVacLoc(vac_loc);
            input_table(vac_data_arrlist);
        }
        //selected selected
        else{//search vac name and location
            vac_data_arrlist = vacc.search_by_details(vac_name, vac_loc);
            input_table(vac_data_arrlist);
        }
    }
    
