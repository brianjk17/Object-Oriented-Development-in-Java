    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name=(String) cmbVaccTemp.getSelectedItem();
        String location=(String) cmbLocationTemp.getSelectedItem(); //get the vaccine name and location
        try{
            Integer.parseInt(txtSupply.getText());//validate if supply is interger
            int supply=Integer.parseInt(txtSupply.getText());
            ArrayList <String> vacArrayList=vacc.search_by_details(name, location);//search by name and location
            Vaccine VC=new Vaccine(vacArrayList.get(0));
            int newSupp= Integer.parseInt(VC.getVacSupp())+supply;//old supply+new supply
            VC.setVacSupp(String.valueOf(newSupp));
            vacc.modify_vacc(VC.return_string());
            
            ArrayList<String> vacArray = vacc.view_all_details();//view all detail
            input_table(vacArray);
            clearArea();
        }
        catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Input Incorrect\nPlease Check Again");
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

