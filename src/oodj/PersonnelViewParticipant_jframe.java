    private void searchParticipant_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchParticipant_btnActionPerformed
        clear_selected();
        ArrayList<String> peep_data=new ArrayList<>();
        try{
            if(id_radiobtn.isSelected()){//search by id
                String id_str = search_txt.getText();
                int id = Integer.parseInt(id_str);
                peep_data = admin.search_peep_byID(id);  //will return the whole data if found, else null;
                if(peep_data==null){
                    JOptionPane.showMessageDialog(null,"Participant Not Found");
                }else{
                    input_table(peep_data);
                }   
            }
            else{   //search by name
                String name = search_txt.getText();
                peep_data = admin.search_peep_byName(name);
                if(peep_data==null){
                    JOptionPane.showMessageDialog(null,"Participant Not Found");
                }else{
                    input_table(peep_data);
                }
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(null,"Incorrect Input");
        }
    }//GEN-LAST:event_searchParticipant_btnActionPerformed

    private void participant_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_participant_tableMouseClicked
        // TODO add your handling code here:
        modify_btn.setEnabled(true);
        clear_btn.setEnabled(true);//makes the button useable
        int i = participant_table.getSelectedRow();
        TableModel model=participant_table.getModel();
        int id = Integer.parseInt(model.getValueAt(i, 0).toString());//get the location from table
        ArrayList<String> selected_data = admin.search_peep_byID(id);
        person = new People(selected_data.get(0));
        id_lbl.setText(String.valueOf(person.getPeepID()));
        name_txtbox.setText(person.getPeepFullname());
        String date=person.getPeepDOB();//gets the date data
        String [] date_detail = date.split("/");
        cmbMonth.setSelectedIndex(Integer.parseInt(date_detail[1])-1);
        cmbDate.setSelectedIndex(Integer.parseInt(date_detail[0])-1);
        cmbYear.setSelectedItem(Integer.parseInt(date_detail[2])); // put the data in the combobox
        if(person.getPeepType().equals("Citizen")){//check the radio button accordingly
            citizen_radiobtn.setSelected(true);
        }else{
            notcitizen_radiobtn.setSelected(true);
        }
        icpass_txtbox.setText(person.getPeepICPass());
        app_lbl.setText(person.getPeepAppid());
        if(person.getPeepStatus().equals("Vaccinated")){//check the radio button accordingly
            vac_radiobtn.setSelected(true);
        }else{
            notvac_radiobtn.setSelected(true);
        }
        if(app_lbl.getText().equals("none")){//if there is appointment
            add_appointment_btn.setEnabled(true);
            vac_radiobtn.setEnabled(false);
        }else{//if no appointment
            add_appointment_btn.setEnabled(false);
            vac_radiobtn.setEnabled(true);
        }
    }//GEN-LAST:event_participant_tableMouseClicked

