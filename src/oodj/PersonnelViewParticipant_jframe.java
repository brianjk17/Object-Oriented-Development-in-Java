    private void input_table(ArrayList<String> peep_data){ //call this function whenever need to update the table
                                //use the arraylist to input table
        if (peep_data==null){
            JOptionPane.showMessageDialog(null,"Participant Not Found");
        }
        else{
            //clear jtable
            DefaultTableModel model = (DefaultTableModel)participant_table.getModel();//The table variable
            model.setRowCount(0);
            for(String data : peep_data){
                //insert a data into table
                String[] data_row = data.split(",");
                String[] selected_data={data_row[0],data_row[3],data_row[7],data_row[8]};   //must select the 3 data in each array into the table
                model.addRow(selected_data);
            }
        }
    }
